    private void txtPesqKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPesqKeyReleased
        if (classe.getClass() == new Fornecedor().getClass()) {
            pesquisarForn();
        } else if (classe.getClass() == new Produto().getClass()) {
            pesquisarProd();
        } else {

        }
    }//GEN-LAST:event_txtPesqKeyReleased

    void pesquisarForn() {
        String t = toUpperCase(val.pegaTxt(txtPesq));
        ArrayList<Fornecedor> l = new ArrayList<>();
        for (Fornecedor o : conForn.lstFornecedor()) {
            String n = toUpperCase(o.getNome());
            if (n.contains(t)) {
                l.add(o);
            }
        }
        conForn.listarNaTabela(tab, l);
    }

    void pesquisarProd() {
        String t = toUpperCase(val.pegaTxt(txtPesq));
        ArrayList<Produto> l = new ArrayList<>();
        for (Produto o : conProd.lstProduto()) {
            String n = toUpperCase(o.getNome());
            if (n.contains(t)) {
                l.add(o);
            }
        }
        conProd.listarNaTabela(tab, l);
    }
    private void tabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabMouseClicked
        if (tab.getSelectedRow() != -1) {
            indice = (Integer) tab.getValueAt(tab.getSelectedRow(), 0);
            String s = "";
            if (classe.getClass() == new Fornecedor().getClass()) {
                selectedForn = (Fornecedor) conForn.pesquisar(indice);
                if (selectedForn != null) {
                    s = selectedForn.getId() + "/" + selectedForn.getNome();
                }
            } else if (classe.getClass() == new Produto().getClass()) {
                selectedProd = (Produto) conProd.pesquisar(indice);
                if (selectedProd != null) {
                    s = selectedProd.getId() + "/" + selectedProd.getNome();
                }
            } else {
            }
            val.poeTxt(txtField, s);
            openFrm(false, x, y);
        } else {
            val.msg(tab, "Erro de listagem.");
        }
    }//GEN-LAST:event_tabMouseClicked

