    private void thisOpen(JTextField txtIn, JComponent c) {
        JComponent a = txtIn;
        if (c != null) {
            a = c;
        }
        a.addMouseListener(new java.awt.event.MouseAdapter() {
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                xm = evt.getX();
                ym = evt.getY();
                x = evt.getXOnScreen() - xm - txtIn.getHeight()-txtIn.getWidth()/2;
                y = evt.getYOnScreen() - ym + txtIn.getHeight();
                openFrm(true, x, y);
            }
        });
    }

    public void openFrm(boolean open, int x, int y) {
        if (open) {
            setLocation(x, y);
            frm.setEnabled(false);
            setVisible(true);
        } else {
            frm.setEnabled(true);
            dispose();
        }
    }

